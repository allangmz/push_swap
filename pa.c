#include "push_swap.h"

void	pa(t_item **a, t_item **b)
{
	t_item	*temp;

	if (*b == NULL)
		return ;
	temp = (*b)->next;
	(*b)->next = *a;
	*a = *b;
	*b = temp;
	return ;
}
