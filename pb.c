#include "push_swap.h"

void	pb(t_item **b, t_item **a)
{
	t_item	*temp;

	if (*a == NULL)
		return ;
	temp = (*a)->next;
	(*a)->next = *b;
	*b = *a;
	*a = temp;
	return ;
}
